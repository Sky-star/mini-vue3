        // 第一次执行时需要将当前的effect保存下来
        // 解决依赖被再次收集导致stop功能失效
        // 当执行stop后就不需要再次进行依赖收集了
        if (!this.active) {
            return this._fn();
        }
        // 每次已经进行依赖收集后就需要将开关关掉
        shouldTrack = true;
